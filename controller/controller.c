void claw(int pos1, int pos2)
{
	if (vexRT[Btn6UXmtr2] == 1)
	{
		motor[Claw] = pos1;
	}
	else if (vexRT[Btn6DXmtr2] == 1)
	{
		motor[Claw] = pos2;
	}
}

void ClawArm(int threshold)
{
	if(abs(vexRT[Ch2Xmtr2]) > threshold)         // If the right joystick is greater than or less than the threshold:
	{
	motor[leftClawArm] = vexRT[Ch2Xmtr2];
	motor[rightClawArm] = vexRT[Ch2Xmtr2];
	}
	else                                    // If the right joystick is within the threshold:
	{
		motor[leftClawArm] = 0;
		motor[rightClawArm] = 0;                // Stop the right motor (cancel noise)
	}
}

void backLift(int threshold)
{
	if(abs(vexRT[Ch2Xmtr2]) > threshold)         // If the right joystick is greater than or less than the threshold:
	{
	motor[goalLift] = vexRT[Ch3Xmtr2];
	}
	else                                    // If the right joystick is within the threshold:
	{
		motor[goalLift] = 0;               // Stop the right motor (cancel noise)
	}
}

void Rotate(int speed2)
{

	if (vexRT[Btn5UXmtr2] == 1)
	{
		motor[rightRotate] = speed2;
		motor[leftRotate] = speed2;
	}
	else if (vexRT[Btn5DXmtr2] == 1)
	{
		motor[rightRotate] = -speed2;
		motor[leftRotate] = -speed2;
	}
	else
	{
		motor[rightRotate] = 0;
		motor[leftRotate] = 0;
	}
}
