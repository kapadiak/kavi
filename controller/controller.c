void driveMode(int choice,int threshold)
{
	displayLCDNumber(1, 10, choice, 2);
	if (choice == 1)
	{
		mecanum_Drive(threshold, speed());
	}
	else
	{
		tank_Drive(threshold, speed());
	}
}

